const TAKE_NOTE_LANGUAGES = ["pt", "en"];

window.takeNoteSite = {
    getLanguage,
    getTranslations: () => TAKE_NOTE_TRANSLATIONS[getLanguage()],
    buildLocalizedUrl
};

document.addEventListener("DOMContentLoaded", () => {
    const currentYear = document.getElementById("current-year");
    const menuButton = document.getElementById("menu-button");
    const siteMenu = document.getElementById("site-menu");

    if (currentYear) {
        currentYear.textContent = new Date().getFullYear();
    }

    bindLanguageSwitcher();
    applyLocalizedContent();
    localizeLinks();

    if (!menuButton || !siteMenu) {
        return;
    }

    const closeMenu = () => {
        siteMenu.classList.remove("is-open");
        menuButton.setAttribute("aria-expanded", "false");
    };

    menuButton.addEventListener("click", () => {
        const isOpen = siteMenu.classList.toggle("is-open");
        menuButton.setAttribute("aria-expanded", isOpen ? "true" : "false");
    });

    siteMenu.querySelectorAll("a, button[data-lang-option]").forEach((item) => {
        item.addEventListener("click", () => {
            if (item.matches("button[data-lang-option]") && window.innerWidth <= 760) {
                return;
            }
            closeMenu();
        });
    });

    window.addEventListener("resize", () => {
        if (window.innerWidth > 760) {
            closeMenu();
        }
    });
});

function getLanguage() {
    const url = new URL(window.location.href);
    const fromQuery = url.searchParams.get("lang");
    if (TAKE_NOTE_LANGUAGES.includes(fromQuery)) {
        localStorage.setItem("takenote-lang", fromQuery);
        return fromQuery;
    }

    const saved = localStorage.getItem("takenote-lang");
    if (TAKE_NOTE_LANGUAGES.includes(saved)) {
        return saved;
    }

    return "pt";
}

function setLanguage(lang) {
    localStorage.setItem("takenote-lang", lang);
    const nextUrl = new URL(window.location.href);
    nextUrl.searchParams.set("lang", lang);
    window.location.href = nextUrl.toString();
}

function buildLocalizedUrl(path, lang) {
    const targetLang = lang || getLanguage();
    const url = new URL(path, window.location.href);
    url.searchParams.set("lang", targetLang);
    return `${url.pathname}${url.search}`;
}

function bindLanguageSwitcher() {
    const lang = getLanguage();
    document.querySelectorAll("[data-lang-option]").forEach((button) => {
        const option = button.getAttribute("data-lang-option");
        button.classList.toggle("is-active", option === lang);
        button.addEventListener("click", () => {
            if (option && option !== lang) {
                setLanguage(option);
            }
        });
    });
}

function applyLocalizedContent() {
    const lang = getLanguage();
    const copy = TAKE_NOTE_TRANSLATIONS[lang];
    const page = document.body.dataset.page;

    document.documentElement.lang = copy.html_lang;

    if (page === "landing") {
        document.title = copy.meta_index_title;
        updateMetaDescription(copy.meta_index_description);
    } else if (page === "privacy") {
        document.title = copy.meta_privacy_title;
        updateMetaDescription(copy.meta_privacy_description);
    } else if (page === "terms") {
        document.title = copy.meta_terms_title;
        updateMetaDescription(copy.meta_terms_description);
    }

    document.querySelectorAll("[data-i18n]").forEach((node) => {
        const key = node.getAttribute("data-i18n");
        if (copy[key]) {
            node.textContent = copy[key];
        }
    });

    document.querySelectorAll("[data-i18n-alt]").forEach((node) => {
        const key = node.getAttribute("data-i18n-alt");
        if (copy[key]) {
            node.setAttribute("alt", copy[key]);
        }
    });
}

function updateMetaDescription(content) {
    const tag = document.querySelector('meta[name="description"]');
    if (tag) {
        tag.setAttribute("content", content);
    }
}

function localizeLinks() {
    const lang = getLanguage();

    document.querySelectorAll("a[data-legal-link]").forEach((link) => {
        const kind = link.getAttribute("data-legal-link");
        if (kind === "privacy") {
            link.setAttribute("href", buildLocalizedUrl("privacy-policy.html", lang));
        } else if (kind === "terms") {
            link.setAttribute("href", buildLocalizedUrl("terms-and-conditions.html", lang));
        }
    });

    document.querySelectorAll('a[href^="index.html"]').forEach((link) => {
        const href = link.getAttribute("href");
        const url = new URL(href, window.location.href);
        url.searchParams.set("lang", lang);
        link.setAttribute("href", `${url.pathname}${url.search}${url.hash}`);
    });
}
